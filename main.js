const RTL_LANGS = new Set(['ar', 'he']);

/* ─── Language Switcher ─────────────────────────────────────────────────────── */
function applyLang(lang) {
  const strings = i18n[lang];
  if (!strings) return;

  // Update dir/lang on html element
  const html = document.documentElement;
  html.setAttribute('lang', lang);
  html.setAttribute('dir', RTL_LANGS.has(lang) ? 'rtl' : 'ltr');

  // Update all translated text nodes
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.dataset.i18n;
    if (strings[key] !== undefined) {
      // Preserve innerHTML for elements with HTML entities (&amp; etc.)
      el.textContent = strings[key];
    }
  });

  // Update active button state
  document.querySelectorAll('.lang-btn').forEach(btn => {
    btn.classList.toggle('active', btn.dataset.lang === lang);
  });

  // Persist choice
  try { localStorage.setItem('tgha-lang', lang); } catch (_) {}
}

function initLang() {
  const saved = (() => { try { return localStorage.getItem('tgha-lang'); } catch (_) { return null; } })();
  const browser = navigator.language?.slice(0, 2);
  const supported = Object.keys(i18n);
  const lang = supported.includes(saved) ? saved
              : supported.includes(browser) ? browser
              : 'en';
  applyLang(lang);
}

document.querySelectorAll('.lang-btn').forEach(btn => {
  btn.addEventListener('click', () => applyLang(btn.dataset.lang));
});

/* ─── Nav: scroll state & active link ──────────────────────────────────────── */
const nav = document.getElementById('nav');
let lastScrollY = window.scrollY;

function onScroll() {
  const y = window.scrollY;
  const scrolledPastNav = y > nav.offsetHeight;

  // Hide on scroll down, show on scroll up
  if (scrolledPastNav) {
    nav.classList.toggle('nav-hidden', y > lastScrollY);
  } else {
    nav.classList.remove('nav-hidden');
  }
  lastScrollY = y;

  nav.classList.toggle('scrolled', y > 20);

  // Highlight active nav link
  const sections = document.querySelectorAll('section[id]');
  let current = '';
  sections.forEach(sec => {
    if (y >= sec.offsetTop - 100) current = sec.id;
  });
  document.querySelectorAll('.nav-links a, .nav-mobile a').forEach(a => {
    const href = a.getAttribute('href')?.replace('#', '');
    a.style.color = href === current ? 'var(--ink)' : '';
  });
}

window.addEventListener('scroll', onScroll, { passive: true });
onScroll();

/* ─── Mobile menu toggle ────────────────────────────────────────────────────── */
const toggle = document.querySelector('.nav-toggle');
const mobileMenu = document.querySelector('.nav-mobile');

toggle.addEventListener('click', () => {
  const expanded = toggle.getAttribute('aria-expanded') === 'true';
  toggle.setAttribute('aria-expanded', String(!expanded));
  mobileMenu.classList.toggle('open', !expanded);
});

// Close mobile menu on link click
mobileMenu.querySelectorAll('a').forEach(a => {
  a.addEventListener('click', () => {
    toggle.setAttribute('aria-expanded', 'false');
    mobileMenu.classList.remove('open');
  });
});

/* ─── Smooth scroll (offset for fixed nav) ──────────────────────────────────── */
document.querySelectorAll('a[href^="#"]').forEach(a => {
  a.addEventListener('click', e => {
    const id = a.getAttribute('href').slice(1);
    const target = document.getElementById(id);
    if (!target) return;
    e.preventDefault();
    const top = target.getBoundingClientRect().top + window.scrollY - nav.offsetHeight;
    window.scrollTo({ top, behavior: 'smooth' });
  });
});

/* ─── Intersection observer: fade-in sections ───────────────────────────────── */
const observer = new IntersectionObserver(
  entries => entries.forEach(entry => {
    if (entry.isIntersecting) {
      entry.target.classList.add('visible');
      observer.unobserve(entry.target);
    }
  }),
  { threshold: 0.08 }
);

document.querySelectorAll('.section, .gallery-item').forEach(el => {
  el.classList.add('fade-in');
  observer.observe(el);
});

/* ─── Init ──────────────────────────────────────────────────────────────────── */
